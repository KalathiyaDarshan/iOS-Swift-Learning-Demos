import Foundation
import UIKit

extension UIColor {
    
    static func appGradient1() -> UIColor {
        return UIColor(named: "appGradient1") ?? .clear
    }
    
    static func appGradient2() -> UIColor {
        return UIColor(named: "appGradient2") ?? .clear
    }
}
